#MODIFY STRINGS

#example1
a = "Hello, World!"
print(a.upper()) #all uppercase

#example2
a = "Hello, World!"
print(a.lower()) #all lowercase

#example3
a = " Hello, World! "
print(a.strip()) #returns "Hello, World!" (Remove Whitespace)

#example4
a = "Hello, World!"
print(a.replace("H", "J")) #turns Hello to Jello

#example5
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']
